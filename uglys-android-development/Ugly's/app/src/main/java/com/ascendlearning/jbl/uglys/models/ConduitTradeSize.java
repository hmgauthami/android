public class ConduitTradeSize {

    private String name;
    private double nipple;
    private double oneWire;
    private double twoWire;
    private double overTwoWire;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getNipple() {
        return nipple;
    }

    public void setNipple(double nipple) {
        this.nipple = nipple;
    }

    public double getOneWire() {
        return oneWire;
    }

    public void setOneWire(double oneWire) {
        this.oneWire = oneWire;
    }

    public double getTwoWire() {
        return twoWire;
    }

    public void setTwoWire(double twoWire) {
        this.twoWire = twoWire;
    }

    public double getOverTwoWire() {
        return overTwoWire;
    }

    public void setOverTwoWire(double overTwoWire) {
        this.overTwoWire = overTwoWire;
    }
}
