public class Bending {

    private String bendingName;
    private int type;
    private String imageName;

    public String getBendingName() {
        return bendingName;
    }

    public void setBendingName(String bendingName) {
        this.bendingName = bendingName;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getImageName() {
        return imageName;
    }

    public void setImageName(String imageName) {
        this.imageName = imageName;
    }
}
